A Atari, responsável pela criação desse jogo, é considerada a empresa pioneira no desenvolvimento de jogos eletrônicos no mundo (ao mesmo foi ela quem popularizou os videogames domésticos). A partir daí, a tecnologia foi evoluindo cada vez mais rápido e o aprimoramento gráfico (design) dos jogos também</p></div>
